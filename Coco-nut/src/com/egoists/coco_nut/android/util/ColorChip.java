package com.egoists.coco_nut.android.util;

public class ColorChip {
    public static int getColor(int category) {
        switch(category) {
        case 0:
            return 0xfff58d7a;
        case 1:
            return 0xff728faf;
        case 2:
            return 0xff79c799;
        case 3:
            return 0xff9e8bb0;
        case 4:
            return 0xffd0b998;
        case 5:
            return 0xff999999;
        case 6:
            return 0xfff5958c;
        case 7:
            return 0xff6faace;
        case 8:
            return 0xff84c992;
        case 9:
            return 0xffad97c8;
        case 10:
            return 0xffe99754;
        case 11:
            return 0xffa2a6aa;
        default:
            return 0x00000000;
        }
    }
    
}
